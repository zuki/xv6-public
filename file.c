// ファイル f に書き込む。
int
filewrite(struct file *f, char *addr, int n)
{
  int r;

  if(f->writable == 0)
    return -1;
  if(f->type == FD_PIPE)
    return pipewrite(f->pipe, addr, n);
  if(f->type == FD_INODE){
    // 最大ログトランザクションサイズを超えないように
    // 数ブロックずつ書き込む。関係するのは、inode, 間接ブロック、
    // 割り当てブロック、非アライン書き込み調整用の
    // 2ブロック。
    // writei()はコンソールなどのデバイスに書き込む場合があるので
    // これは本当に低速の部類に入る。
    int max = ((MAXOPBLOCKS-1-1-2) / 2) * 512;
    int i = 0;
    while(i < n){
      int n1 = n - i;
      if(n1 > max)
        n1 = max;

      begin_op();
      ilock(f->ip);
      if ((r = writei(f->ip, addr + i, f->off, n1)) > 0)
        f->off += r;
      iunlock(f->ip);
      end_op();

      if(r < 0)
        break;
      if(r != n1)
        panic("short filewrite");
      i += r;
    }
    return i == n ? n : -1;
  }
  panic("filewrite");
}
